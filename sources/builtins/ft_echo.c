void	ft_echo(char **cmd)
{
	int		i;
	bool	n;

	n = false;
	if (cmd[1] == "-n")
		n = true;
	if (!cmd)
	{
		ft_printf("%s\n", cmd[2]);
		if(n = true)
			printf("\n");
	}
}